package JAVA_BASICS;
//Divide two int values vs two double values and compare results.
public class Q4 {
    public static void main(String[] args){
        int int1 = 7;
        int int2 = 2;
        double double1 = 7.0;
        double double2 = 2.0;

        int intresult = int1 / int2;
        double doubleresult = double1 / double2;

        System.out.println("Integer division result: " + intresult);
        System.out.println("Double division result: " + doubleresult);

    }
}    

